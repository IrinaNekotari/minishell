#include "minishell.h"

void	part1_counter(int *i, char *s, char *quote, int *quoted)
{
	if ((s[(*i)] == '\"' || s[(*i)] == '\'') && (*quoted) == 0)
	{
		if ((s[(*i) - 1] != '\\') || (*i) == 0)
		{
			(*quoted) = 1;
			(*quote) = s[(*i)];
		}
	}
	else if (s[(*i)] == (*quote) && (*quoted) == 1
		&& ((s[(*i) - 1] != '\\') || (*i) == 0))
		(*quoted) = 0;
}
